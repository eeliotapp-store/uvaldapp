export function BottomNav() {
  const pathname = usePathname();
  const employee = useAuthStore((state) => state.employee);
  const [showMore, setShowMore] = useState(false);

  const filteredMoreNav = moreNavigation.filter(
    (item) => !item.ownerOnly || isOwner(employee?.role)
  );

  // Verificar si alguna ruta del menú "Más" está activa
  const isMoreActive = filteredMoreNav.some(item => pathname === item.href);

  return (
    <>
      {/* Overlay para cerrar el menú */}
      {showMore && (
        <div
          className="lg:hidden fixed inset-0 bg-black/50 z-40"
          onClick={() => setShowMore(false)}
        />
      )}

      {/* Menú expandido */}
      {showMore && (
        <div className="lg:hidden fixed bottom-16 left-0 right-0 bg-white border-t border-gray-200 z-50 max-h-[60vh] overflow-y-auto shadow-lg rounded-t-2xl">
          <div className="p-4 grid grid-cols-4 gap-3">
            {filteredMoreNav.map((item) => {
              const isActive = pathname === item.href;
              return (
                <Link
                  key={item.name}
                  href={item.href}
                  onClick={() => setShowMore(false)}
                  className={cn(
                    'flex flex-col items-center justify-center p-3 rounded-xl',
                    isActive
                      ? 'bg-amber-100 text-amber-600'
                      : 'text-gray-600 hover:bg-gray-100'
                  )}
                >
                  <item.icon className="w-6 h-6" />
                  <span className="text-xs mt-1 text-center leading-tight">{item.name}</span>
                </Link>
              );
            })}
          </div>
        </div>
      )}

      {/* Barra de navegación fija */}
      <nav className="lg:hidden fixed bottom-0 left-0 right-0 bg-white border-t border-gray-200 z-50">
        <div className="flex justify-around items-center h-16">
          {mainNavigation.map((item) => {
            const isActive = pathname === item.href;
            return (
              <Link
                key={item.name}
                href={item.href}
                className={cn(
                  'flex flex-col items-center justify-center w-full h-full',
                  isActive ? 'text-amber-600' : 'text-gray-500'
                )}
              >
                <item.icon className="w-6 h-6" />
                <span className="text-xs mt-1">{item.name}</span>
              </Link>
            );
          })}

          {/* Botón "Más" */}
          <button
            onClick={() => setShowMore(!showMore)}
            className={cn(
              'flex flex-col items-center justify-center w-full h-full',
              showMore || isMoreActive ? 'text-amber-600' : 'text-gray-500'
            )}
          >
            <MoreIcon className="w-6 h-6" />
            <span className="text-xs mt-1">Más</span>
          </button>
        </div>
      </nav>
    </>
  );
}

